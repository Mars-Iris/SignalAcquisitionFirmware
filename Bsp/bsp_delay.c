#ifdef  STC12C5A60S2

//========================================================================
// 函数: void Delay1ms()		//@11.0592MHz
// 描述: 延时1毫秒函数
// 参数：无
// 返回: 无
// 版本: V1.0, 2022-10-17
//========================================================================
static void Delay1ms()		//@11.0592MHz
{
	unsigned char i, j;

	_nop_();
	i = 11;
	j = 190;
	do
	{
		while (--j);
	} while (--i);
}
//========================================================================
// 函数: delay_ms(u16 count)		//@11.0592MHz
// 参数：count,为需要延时的毫秒数，最大值为65535.
// 返回: 无
// 版本: V1.0, 2022-10-17
//========================================================================
void delay_ms(u16 count)		//@11.0592MHz
{
		while(count--)
		{
			Delay1ms();
		}
}

#endif


